// String and Number Methods

//lenght
var text = 'Always look on the bright side of life!';
console.log(text.length);

//indexOf
var pos = text.indexOf("bright");
console.log(pos);

//slice
var part = text.slice(7,11);
console.log(part);

//replace
var newText = text.replace('bright', 'good');
console.log(newText);

//toUpperCase
var upper = text.toUpperCase();
console.log(upper);

//toFixed
var num = 99.9666;
console.log(num.toFixed(2));
console.log(num.toFixed(0));

