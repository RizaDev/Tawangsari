//Number
var x = 2;
var y = 3;
var z = x + y;
console.log(z)

var s = 10, t = 20, z = 30; //you can declare many variables in one statement.
console.log(s + t + z);

//String
var fName = 'Peter'; 
var sName = "Smith"; // you can use single or double quotes too
console.log(fName + " " + sName);

//Boolean
var n1 = 3;
var n2 = 3;
var n3 = 6;
console.log(n1 == n2); // Returns true
console.log(n1 == n3); // Returns false

// undefined, null & dynamic typing
var v;            // Now v is undefined
console.log(v);
v = 11;           // Now v is a Number
console.log(v);
v = 'Kevin';      // Now v is a String 
console.log(v);
v = null;         // Now v is a null 
console.log(v);


