// Arithmetic Operators
var a = 33;
var b = 10;
console.log(a + b); // Addition
console.log(a / b);  // Division

var x = 5;
var z = x ** 3; // Exponentiation
console.log(z); // The result is (5*5*5) 125 

// Assignment Operators
console.log(a += 17); // Addition assignment
console.log(b *= 2); // Multiplication assignment 

// Comparison Operators
console.log(a==b); // equal to - false
console.log(a>b); // greater than - true

// Logical Operators
console.log(a>b && z<x); // logical AND - false
console.log(!(a>b && z<x)); // logical NOT and logical AND - true

// Type Operators
console.log(typeof a);
var name = 'Kevin';
console.log(typeof name);

// Conditional Operator
var age = 16;
var alcoholPermitted = (age < 18) ? 'Too young':'Old enough'; 
console.log(alcoholPermitted + ' to drink alcohol');


