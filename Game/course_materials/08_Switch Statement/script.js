// switch statement 

var month = 'June';

switch (month) {
    case 'June':
        console.log('Summer begins');
        break;

    case 'July':
        console.log('Middle of the summer');
        break;

    case 'August':
        console.log('Last month of Summer');
        break;

    default:
        console.log('This is not a summer month');
        break;
}







